public class _2_symbols {
    public static void main(String[] args) {
        // new line \n
        System.out.println("hello\nworld"); //prints each word on separate line
        // tabulator \t
        System.out.println("hello\tworld"); //prints: hello  world
        // double quote \"
        System.out.println("hello \"world\""); //prints: hello "world"
        // quote \'
        System.out.println("hello \'world\'"); //prints: hello 'world'
    }
}
